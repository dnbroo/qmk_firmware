// Draw to OLED
bool oled_task_user() {
    // Set cursor position
    

    // Switch on current active layer
    switch (get_highest_layer(layer_state)) {
        case L_DEFAULT :
            if(layer_trigger){
              layer_trigger = false;
              oled_clear();
            }
            oled_set_cursor(0, 1);

            // left side
            render_L_DEFAULT_animation();

            // right side
            // render_R_DEFAULT_animation();
            // oled_write_P(PSTR("WPM:"), false);
            // oled_write(get_u8_str(get_current_wpm(), ' '), false);
            break;
        case L_FUNC :
            oled_clear(); 
            oled_write("1\nFunc\nLayer", false);
            layer_trigger = true;
            break;
        case L_SYMBOLS :
            oled_clear();
            oled_write("\n\n2\nSym\nLayer", false);
            layer_trigger = true;
            break;
        case L_RGB :
            oled_clear();
            oled_write("\n\n\n\n3\nRGB\nLayer", false);
            layer_trigger = true;
            break;
        case L_ARROW :
            oled_clear();
            oled_write("\n\n\n\n\n\n4\nArrowLayer", false);
            layer_trigger = true;
            break;
        case L_GAME :
            oled_clear();
            oled_write("Good\n Luck\n\n\n\n\n\n5\nGame\nLayer", false);
            layer_trigger = true;
            break;
        case L_GAME_2 :
            oled_clear();
            oled_write("\n\n\n\n\n\n\n\n\n\n6\nGame 2\nLayer", false);
            layer_trigger = true;
            break;
    }

    return false;
}

